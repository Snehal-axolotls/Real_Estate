class PropertyModel {
  final String imageUrl;
  final String title;
  final String location;
  final int area;
  final int bedrooms;
  final int bathrooms;
  final int parkings;
  final String price;

  PropertyModel({
    required this.imageUrl,
    required this.title,
    required this.location,
    required this.area,
    required this.bedrooms,
    required this.bathrooms,
    required this.parkings,
    required this.price,
  });
}
